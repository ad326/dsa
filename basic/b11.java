// Sum using function
public class b11 {
    public static int sumi(int a, int b){ // parameters or formal parameters
        return a+b;
    }
    public static void main(String[] args) {
        int a=5;
        int b=6;

        int sum = sumi(a, b); // arguments or actual parameters

        System.out.println("The sum is: "+sum);
    }
}
